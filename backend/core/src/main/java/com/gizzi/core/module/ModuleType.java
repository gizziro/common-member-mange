package com.gizzi.core.module;

// 모듈 유형 열거형
// SINGLE: 시스템 전체에서 인스턴스 없이 하나만 존재 (예: 대시보드, 설정)
// MULTI: 인스턴스를 생성하여 여러 개 운영 (예: 게시판, 블로그)
public enum ModuleType
{

	//----------------------------------------------------------------------------------------------------------------------
	// [ 열거 값 ]
	//----------------------------------------------------------------------------------------------------------------------

	SINGLE,   // 단일 모듈 — 인스턴스 생성 불가
	MULTI     // 다중 인스턴스 모듈 — 인스턴스 생성 가능
}
