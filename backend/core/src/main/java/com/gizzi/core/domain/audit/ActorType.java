package com.gizzi.core.domain.audit;

// 감사 로그 행위자 유형 열거형
// 각 감사 로그에 행위를 수행한 주체가 사용자인지 시스템인지 구분한다
public enum ActorType
{
	//----------------------------------------------------------------------------------------------------------------------
	// [ 행위자 유형 ]
	//----------------------------------------------------------------------------------------------------------------------

	USER,		// 로그인한 사용자에 의한 행위
	SYSTEM		// 시스템 자동 처리에 의한 행위 (스케줄러, 자동 잠금 해제 등)
}
